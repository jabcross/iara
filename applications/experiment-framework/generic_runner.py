#!/usr/bin/env python3
"""
Generic experiment runner - works with any application via YAML configuration.

This runner implements the experiment framework in a completely generic way,
driven entirely by experiments.yaml. No application-specific scripts needed.

Usage:
  python3 generic_runner.py <app_dir> --list
  python3 generic_runner.py <app_dir> -e <experiment_set>

Example:
  python3 generic_runner.py applications/05-cholesky --list
  python3 generic_runner.py applications/05-cholesky -e regression
"""

import sys
import os
from pathlib import Path
from typing import Dict, Any

from app_runner import AppExperimentRunner
from base_runner import create_cli_parser


class GenericExperimentRunner(AppExperimentRunner):
    """
    Completely generic experiment runner that works with any application.

    This runner reads everything from experiments.yaml and generates test
    names matching what generate_experiments.py creates, without needing
    any application-specific logic.
    """

    def __init__(self, app_dir: str, yaml_config_path: str, app_name: str, app_filter: str):
        """
        Initialize generic runner.

        Args:
            app_dir: Path to application directory (e.g., applications/05-cholesky)
            yaml_config_path: Path to experiments.yaml
            app_name: Name of application (e.g., '05-cholesky', 'cholesky')
            app_filter: CMake filter for test selection
        """
        super().__init__(yaml_config_path, app_name, app_filter)
        self.app_dir = Path(app_dir)

        # Track which experiment set we're running (set during run())
        self.current_experiment_set = None

    def create_test_directory(self, params: Dict[str, Any]) -> Path:
        """
        Stub implementation - test directories are created by CMake.

        The actual test building happens via CMake's iara_add_test_instance()
        calls that were generated by scripts/generate_experiments.py and are
        defined in applications/<app>/experiment/CMakeLists.txt.

        This method is kept for framework compatibility but does nothing.
        """
        # Return a dummy path - not used for actual building
        return Path("stub")

    def get_test_name_from_params(self, params: Dict[str, Any]) -> str:
        """
        Generate test name matching what generate_experiments.py creates.

        Pattern: <app>_<experiment_set>_<scheduler>_<param1>_<value1>_<param2>_<value2>_...

        Only includes parameters defined in the experiment_set, excluding computed parameters.

        Example: 05-cholesky_regression_vf-omp_matrix_size_2048_num_blocks_4
        """
        if not self.current_experiment_set:
            raise RuntimeError("current_experiment_set not set")

        # Extract app name from app_dir
        # Handle both "applications/05-cholesky" and "05-cholesky" formats
        app_name = self.app_dir.name
        if app_name.startswith('applications/'):
            app_name = app_name.replace('applications/', '')

        # Find the experiment set definition to know which parameters to include
        exp_sets = {s['name']: s for s in self.config.get('experiment_sets', [])}
        exp_set = exp_sets.get(self.current_experiment_set, {})
        exp_params = exp_set.get('parameters', {})

        # Get list of computed parameters to exclude
        computed_param_names = {cp['name'] for cp in self.config.get('computed_parameters', [])}

        # Build parameter string
        param_parts = [app_name, self.current_experiment_set]

        # Add scheduler first if present and in experiment set (common convention)
        if 'scheduler' in params and 'scheduler' in exp_params:
            param_parts.append(str(params['scheduler']))

        # Add other parameters in sorted order (exclude computed parameters)
        for key in sorted(params.keys()):
            if key != 'scheduler' and key not in computed_param_names:
                value = params[key]
                param_parts.append(f"{key}_{value}")

        return "_".join(param_parts)

    def get_parameter_exports(self, params: Dict[str, Any]) -> Dict[str, str]:
        """
        Map parameters to environment variables based on parameter_metadata.

        Reads export_as mappings from experiments.yaml parameter_metadata section.
        """
        exports = {}
        param_metadata = self.config.get('parameter_metadata', {})

        for param_name, param_value in params.items():
            if param_name in param_metadata:
                export_as = param_metadata[param_name].get('export_as')
                if export_as:
                    exports[export_as] = str(param_value)
            else:
                # Fallback: use uppercase parameter name
                exports[param_name.upper()] = str(param_value)

        return exports

    def run(self, experiment_set_name: str = None) -> None:
        """
        Run a complete experiment set.

        Override to track which experiment set we're running (needed for test names).
        """
        self.current_experiment_set = experiment_set_name
        super().run(experiment_set_name)


def main():
    """Main entry point for generic runner."""
    parser = create_cli_parser()
    parser.add_argument('app_dir', help='Path to application directory')
    args = parser.parse_args()

    app_dir = Path(args.app_dir)
    if not app_dir.exists():
        print(f"ERROR: Application directory not found: {app_dir}", file=sys.stderr)
        sys.exit(1)

    # Find experiments.yaml
    yaml_path = app_dir / 'experiment' / 'experiments.yaml'
    if not yaml_path.exists():
        print(f"ERROR: experiments.yaml not found at: {yaml_path}", file=sys.stderr)
        sys.exit(1)

    # Extract app name from directory
    app_name = app_dir.name
    app_filter = app_name

    try:
        runner = GenericExperimentRunner(
            str(app_dir),
            str(yaml_path),
            app_name,
            app_filter
        )

        if args.list:
            runner.run()
        else:
            runner.run(args.experiment_set)

    except Exception as e:
        print(f"ERROR: {e}", file=sys.stderr)
        import traceback
        traceback.print_exc()
        sys.exit(1)


if __name__ == '__main__':
    main()
